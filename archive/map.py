def grid_to_mm(grid_pos):
    """Convert grid coordinates to millimeters for robot navigation."""
    x_mm = grid_pos[0] * GRID_CELL_SIZE
    y_mm = grid_pos[1] * GRID_CELL_SIZE
    return (x_mm, y_mm)
